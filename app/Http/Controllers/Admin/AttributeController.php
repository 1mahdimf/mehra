    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $attributes = Attribute::query()->with(['values','products'])->get();
        if(\request()->ajax()){
            foreach ($attributes as $attribute) {
                if($attribute->values){
                    foreach ($attribute->values as $valueKey => $value){
                        $attribute->values[$valueKey] = $value->value;
                    }
                }
            }
            return DataTables::of($attributes)
                ->editColumn('type',function ($attribute){
                   if($attribute->type){
                       return AttributeType::getDescription($attribute->type);
                   }
                })
                ->make();
        }
        return view('admin.attributes.index', compact('attributes'));
    }

